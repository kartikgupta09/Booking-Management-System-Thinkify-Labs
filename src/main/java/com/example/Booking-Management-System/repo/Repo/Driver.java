class Driver {
    String name;
    String gender;
    int age;
    String vehicleDetails;
    int[] currentLocation;

    Driver(String name, String gender, int age, String vehicleDetails, int[] currentLocation) {
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.vehicleDetails = vehicleDetails;
        this.currentLocation = currentLocation;
    }
}
